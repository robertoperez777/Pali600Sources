public class MySuperClass
{
	//////////////////////////////////////
	//////// Class level variables
	//////////////////////////////////////
	protected static String classVar = "MySuperClass";
	
	//////////////////////////////////////
	//////// Class level initialization
	//////////////////////////////////////
	static{
		classVar = "Type var.";
	}
	
	//////////////////////////////////////
	//////// Member variables
	//////////////////////////////////////
	protected String var;

	//////////////////////////////////////
	//////// Constructor
	//////////////////////////////////////
	
	public MySuperClass(String var)
	{
		// 
		super();
		this.var = var;
	}
	
}
